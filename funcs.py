def add_two_numbers():
    x = int(input("Введите первое число: "))
    y = int(input("Введите второе число: "))
    return print(f"Результат сложения: {x + y}")



def subtract_numbers():
    x = int(input("Введите первое число: "))
    y = int(input("Введите второе число: "))

    return print(f"Результат вычитания: {x - y}")
